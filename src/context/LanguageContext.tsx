const LanguageContext = createContext<LanguageContextValue>({
  language: 'es',
  setLanguage: () => {},
  t: (key: string, params?: Record<string, string | number>) => key,
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>('es');

  const t = (key: string, params?: Record<string, string | number>): string => {
    let translation = key;

    // Usar siempre las traducciones embebidas como fuente principal
    translation = translations[language][key as keyof typeof translations['es']] || 
                 additionalEsTranslations[key as keyof typeof additionalEsTranslations] || 
                 key;
    
    // Interpolaci√≥n de variables: reemplaza {variable} con el valor
    if (params) {
      Object.keys(params).forEach(param => {
        const regex = new RegExp(`{${param}}`, 'g');
        translation = translation.replace(regex, String(params[param]));
      });
    }
    
    return translation;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export const useLanguage = () => useContext(LanguageContext);